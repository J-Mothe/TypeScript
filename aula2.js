// Declarando nossa variável 
var x = 3.14;
var y = 2;
//Realizando os cálculos
var soma = x + y; // Soma X + Y
var subt = x - y; // Subtrai X - Y
var mult = x * y; // Multiplica X por Y
var expo = Math.pow(x, y); // Eleva X à Y
var mod = x % y; // Captura o resto
// Imprimindo o valor de nossa variável
console.log("Soma: " + soma);
console.log("Subtração: " + subt);
console.log("Multiplicação: " + mult);
console.log("Exponencial: " + expo);
console.log("Resto: " + mod);
