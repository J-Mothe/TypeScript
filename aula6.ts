// Declarando nossas varíaveis
let maria : string = "foi a praia";
let joao : string = "foi a praia";

// Testando nossas varíaveis com E (&&)
if (maria === "foi a praia" && joao === "foi a praia") {
    console.log("Joao e Maria foram a praia");

}

// Testando nossas varíaveis com OU (||)
if (maria === "foi a praia" || joao === "foi a praia") {
    console.log("Alguem foi a praia");

}